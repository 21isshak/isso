#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;

	if (str == 0)
	{
		ft_putstr("(null)");
		return (6);
	}
	i = 0;
	while (str[i] != '\0')
	{
		write (1, &str[i], 1);
		i++;
	}
	return (i);
}
